#!/usr/bin/node
const DataSquare = require('./5-square.js');

module.exports = class Square extends DataSquare {
  charPrint (c) {
    if (c === undefined) {
      this.print();
    } else {
      for (let i = 0; i < this.height; i++) {
        console.log(c.repeat(this.width));
      }
    }
  }
};
